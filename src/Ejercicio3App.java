public class Ejercicio3App {

	public static void main(String[] args) {
		
		int x = 9;
		int y = 3;
		double n = 22.6;
		double m = 36.8;
		
		System.out.println("Variable x = " + x + " | Variable y = " + y + " | Variable n = " + n + " | Variable m = " + m);
		System.out.println("x + y = " + (x + y));
		System.out.println("x - y = " + (x - y));
		System.out.println("x * y = " + (x * y));
		System.out.println("x / y = " + (x / y));
		System.out.println("x % y = " + (x % y));
		System.out.println("n + m = " + (n + m));
		System.out.println("n - m = " + (n - m));
		System.out.println("n * m = " + (n * m));
		System.out.println("n / m = " + (n / m));
		System.out.println("n % m = " + (n % m));
		System.out.println("x + n = " + (x + n));
		System.out.println("y / m = " + (y / m));
		System.out.println("y % m = " + (y % m));
		System.out.println("Doble de variable x = " + (x * 2) + " | Doble de variable y = " + (y * 2) + " | Doble de variable n = " + (n * 2) + " | Doble de variable m = " + (m * 2));
		System.out.println("Suma de todas las variables = " + (x + y + n + m));
		System.out.println("Producto de todas las variables = " + (x * y * n * m));
	}

}
